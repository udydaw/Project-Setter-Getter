public class Gerabah {
    String atasnama;
    String jenisgerabah;
    int jumlahgerabah;
    int hargagerabah;
    int totalharga;

    public String getAtasNama() {
        return atasnama;
    }

    public void setAtasNama(String atasnama) {
        this.atasnama = atasnama;
    }

    public String getJenisGerabah() {
        return jenisgerabah;
    }

    public void setJenisGerabah(String jenisgerabah) {
        this.jenisgerabah = jenisgerabah;
    }

    public int getJumlahGerabah() {
        return jumlahgerabah;
    }

    public void setJumlahGerabah(int jumlahgerabah) {
        this.jumlahgerabah = jumlahgerabah;
    }
    
    public int getHargaGerabah() {
        return hargagerabah;
    }

    public void setHargaGerabah(int hargagerabah) {
        this.hargagerabah = hargagerabah;   
    }
    
    public int getTotalHarga() {
       return totalharga;
    }
        
    public void setTotalHarga(int totalharga) {
        this.totalharga = totalharga;   
    }
 }